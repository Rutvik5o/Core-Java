public class StaticArr {

    public static void main(String []args)
    {
        int a[]=new int[5];

        a[0]=10;
        a[1]=20;
        a[2]=30;
        a[3]=05;
        a[4]=11;

        for(int i=0;i<5;i++)
        {
            System.out.println(a[i]);
        }
    }
    
}
